# Arithmetic Operators Example

a = 20
b = 6

sum_result = a + b        # Addition
diff_result = a - b       # Subtraction
prod_result = a * b       # Multiplication
div_result = a / b        # Division
mod_result = a % b        # Modulus
floor_div = a // b        # Floor division
power = a ** 2            # Exponentiation

print("Arithmetic Operators Output")
print(sum_result)
print(diff_result)
print(prod_result)
print(div_result)
print(mod_result)
print(floor_div)
print(power)