# Implicit Type Casting Example
# Python automatically converts one data type into another

a = 10              # Integer value
b = 5.5             # Float value

result = a + b      # Integer is converted into float automatically

print("Value of a:", a)
print("Value of b:", b)
print("Result:", result)

print("Type of a:", type(a))
print("Type of b:", type(b))
print("Type of result:", type(result))

x = 3               # Integer
y = 2.0             # Float

sum_value = x + y
print("Sum:", sum_value)
print("Type of sum:", type(sum_value))